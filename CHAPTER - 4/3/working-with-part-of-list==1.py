'''::::::::::::::::::::::::::::::: SLICING A LIST ::::::::::::::::::::::::::::::::::'''

players = ['Rishabh', 'Priyanshu', 'Manish', 'Vikash', 'Rishu']

'''WE CAN GENERATE ANY SLICE OF LISTS BY DOING THIS ============== '''

print(players[0:3])

players = ['Rishabh', 'Priyanshu', 'Manish', 'Vikash', 'Rishu']

print(players[:5])

players = ['Rishabh', 'Priyanshu', 'Manish', 'Vikash', 'Rishu']

print(players[2:])


''' This will print the last three element of an array '''

